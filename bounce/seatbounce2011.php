The 2011 General Election was a Sea Change in Irish Politics with the decline of the Fianna F&aacute;il Vote to its lowest ever. It was also the most successful election in seats returned for Fine Gael. And the most successful for the modern day Sinn F&eacute;in.
Other things to note was the massive increase in the Labour party percentage of the vote and seats returned (while slightly less than the 1992 election if you combine the Democratic Left and the Labour party they returned the same amount of seats. Another Casualty of this election has been the Green party which lost all its seats).
</p><p>
The big winner on seats was Fine Gael and the big loser was Fianna F&aacute;il (who have benefitted a great deal in recent elections from a Seat bounce) this is down to the transfer unfriendliness of Fianna F&aacute;il particularly in lower preference votes, but also in running two many candidates in the case of Donegal South West, Dublin South East, Dun Laoghaire and perhaps Wicklow where a single candidate would have stayed in the race longer.</p>

Note: Above the figures of the Socialist Party and People Before Profit are separate, since the ULA is note registered as a political party and some of its candidates were listed as non-party are included in the independents/others e.g. Seamus Healy TD.
<p>
Could Fine Gael have got an overall majority, well it would have been extremely unlikely, they did however have excellent vote management (Mayo, Wicklow, Dublin South, Cavan-Monaghan, Meath East, Meath West & Louth), but in some cases they missed seats, Dublin North West they ran two many candidates, where they didnt have a seat, and it is the only constituency where they dont have a seat, Dublin South Central they alow ran to many candidates and only got one seat (a 4 candidate strategy in Galway West nearly cost them a seat as well), but Galway East, Cork South Central, Clare, and to a lesser extent Tipperary South, Kerry North and South as well as Kildare South with a 2nd candidate with a bit of luck they could have taken extra seats. Labours change of tone in the last week of trying to prevent a single party government may also have helped prevent an overall majority
</p>
